#Int para Float
price = 10
print(price)
# 10

price = float(price)
print(price)
# 10.0

price = 10 / 2
print(price)
# 5.0

# Float para Int

price = 10.30
print(price)
# 10.30

price = int(price)
print(price)
# 10

# Conversão por divisão

price = 10
print(price)
# 10

print(price / 2)
# 5.0

print(price // 2)
# 5 (nesta conversão com duas barras, ele conserva o int

